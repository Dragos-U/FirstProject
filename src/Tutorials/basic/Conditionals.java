package Tutorials.basic;

public class Conditionals {
	public static void main(String[] args) {
		
		// if 
		boolean sayHello = false;
		
		
		
		if (sayHello) {
			System.out.println("Hello");
		}
		else { 
			System.out.println("Good bye");
			
		if (true && true) {
			System.out.println("and");
		}
		
		if (true || false) {
			System.out.println("or");
		}
		}
	}
}
